<section id="quatrieme">
    <p>
        Son père prend en charge son éducation musicale. Mozart commence à jouer des menuets vers cinq ans.
        Conscient de la précocité et du talent de son fils, Léopold Mozart le présente à la cour de Vienne en 1762,
        qui s’émerveille devant le tout jeune musicien. Il décide alors de l’emmener avec sa sœur faire le tour des
        capitales européennes.
    </p>
</section>
<section id="cinquieme">
    Entre 1763 et 1771, Léopold et ses deux enfants prodiges passeront par de nombreux pays : l’Allemagne, la Belgique,
    la France, les Pays-bas, l’Angleterre et l’Italie. Ces voyages et les rencontres avec d’autres musiciens influenceront
    fortement le jeune Mozart. Il fait la connaissance notamment de Johann Schobert et Jean-Chrétien Bach, le fils de Jean-Sébastien
    Bach et parfait à leurs côtés ses connaissances musicales. Mozart compose ses premiers opéras alors qu’il vient d’avoir 11 ans !
