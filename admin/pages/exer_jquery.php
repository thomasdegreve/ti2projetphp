<section id="troisieme">
    <p>
        La musique est une histoire de famille chez les Mozart. Son père Léopold était un excellent violoniste
        et occupait le poste de second violon dans l'orchestre de la cour du prince-archevêque de Salzbourg.
        Dès trois ans, Mozart pianote sur le clavecin de sa sœur, Marie-Anna, et montre très rapidement d’excellentes
        dispositions  pour la musique. Il possède une mémoire et une capacité de concentration remarquables, ainsi qu’une
        oreille absolue.</p>
